using namespace std;

void solve(){
    ll n,ct=0,ans=1;
    cin>>n;
    while(n!=1){
       if(n%2) --n;
       n/=2;
       ct++; 
    }
    for(int i=1;i<=ct;i++){
        ans*=2;
    }
    cout<<ans-1<<endl;
}

